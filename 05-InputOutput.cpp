#include<iostream>
using namespace std;

int main() {
    // (\n is used for line change same as endl)
    // (<<this is called insertion operator)
    // (>>this is called extration operator)
int num1,num2;
cout<<"Enter the value of Num1: \n";
cin>>num1;
cout<<"Enter the value of Num2: \n";
cin>>num2;
cout<<"The sum is: \n"<<num1+num2;
return 0;
}